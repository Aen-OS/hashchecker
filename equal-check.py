def equal_check():
    first = input("Please input first value/string \n")
    second = input("Please input second value/string \n")
    if first == second:
        print("These two values/strings are equal")
        print(first)
        print(second)
    else:
        print("These two values/strings are NOT equal")
        print(first)
        print(second)

equal_check()
